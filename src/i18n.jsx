const LanguageContext = createContext(null)

function getNestedValue(obj, path) {
  return path.split('.').reduce((value, key) => value?.[key], obj)
}

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => {
    if (typeof window === 'undefined') return 'sv'
    return window.localStorage.getItem(STORAGE_KEY) || 'sv'
  })

  const currencyFormatter = useMemo(() => new Intl.NumberFormat(
    language === 'sv' ? 'sv-SE' : 'en-US',
    { style: 'currency', currency: 'SEK' }
  ), [language])

  const value = useMemo(() => ({
    language,
    setLanguage: nextLanguage => {
      setLanguage(nextLanguage)
      if (typeof window !== 'undefined') {
        window.localStorage.setItem(STORAGE_KEY, nextLanguage)
      }
    },
    formatCurrency: (amount, lang = language) => {
      const formatter = lang === language
        ? currencyFormatter
        : new Intl.NumberFormat(lang === 'sv' ? 'sv-SE' : 'en-US', {
            style: 'currency',
            currency: 'SEK',
          })
      return formatter.format(Number(amount) || 0)
    },
    t: path => {
      const value = getNestedValue(translations[language], path)
      return value ?? path
    },
  }), [currencyFormatter, language])

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useTranslation() {
  const context = useContext(LanguageContext)

  if (!context) {
    throw new Error('useTranslation must be used within LanguageProvider')
  }

  return context
}
